public class NumberDef {
    
    public void displayTwiceTheNumber(int x, int y){
        
        System.out.println("2 * " + x + " = " + (2 * x));
        
        System.out.println("2 * " + y + " = " + (2 * x));
   
    }
    
    public void displayNumberPlusFive(int a, int b){
        
        System.out.println(a + " + 5 =" + (a + 5));
        
        System.out.println(b + " + 5 =" + (b + 5));
        
        
    }
    
    public void displayNumberSquared(int m, int n){
        
        System.out.println(m + " * " + m + " = " + (m * m) );
        
        System.out.println(n + " * " + n + " = " + (n * n));
        
    }
}
